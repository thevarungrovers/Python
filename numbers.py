# import libraries
import math
import random

# to get absolute value
print(abs(-10))

# get smallest value
print(math.ceil(10.98))
print(math.ceil(10.28))

# get largest integer
print(math.floor(10.98))
print(math.floor(10.28))

# log value
print(math.log(15))

# maximum value
tup = [1,89,-4]
print(max(tup))
#min value
print(min(tup))

# get fractional and integer value
print(math.modf(10.98))

# return power of number
print(math.pow(2,6))

# round number
print(round(10.56))
print(round(10.568948,3))

# squareroot
print(math.sqrt(100)) 

#value of pi
print(math.pi)


